`use strict`

  function add(a, b, c, d, e) {
    return a + b + c + d + e;
  }
  
  function subtract(a, b, c, d, e) {
    return a - b - c - d - e;
  }
  
  function multiply(a, b, c, d, e) {
    return a * b * c * d * e;
  }
  
  function divide(a, b, c, d, e) {
    return a / b / c / d / e;
  }
  
  module.exports = { add, subtract, multiply, divide };